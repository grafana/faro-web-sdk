const { existsSync, readFileSync, writeFileSync } = require('node:fs');
const { createRequire } = require('node:module');
const { dirname, join } = require('node:path');

// Reads the version of the bundled `rrweb` package at build time and writes it
// to `src/rrwebVersion.ts`. This mirrors `packages/core/bin/genVersion.js`.
//
// rrweb does not export a runtime version constant, and its `exports` map blocks
// `require('rrweb/package.json')`, so we resolve the package's main entry and
// walk up the directory tree to find its `package.json` on disk instead.

if (process.env['SKIP_GEN_VERSION'] && process.env['SKIP_GEN_VERSION'] === '1') {
  console.info('Skipping generating rrweb version file due to "SKIP_GEN_VERSION" environment variable');
  process.exit(0);
}

let version = '';

try {
  const req = createRequire(join(process.cwd(), 'package.json'));

  let dir = dirname(req.resolve('rrweb'));
  let packageJsonPath = '';

  while (dir !== dirname(dir)) {
    const candidate = join(dir, 'package.json');
    if (existsSync(candidate)) {
      const pkg = JSON.parse(readFileSync(candidate, 'utf8'));
      if (pkg.name === 'rrweb') {
        version = pkg.version;
        packageJsonPath = candidate;
        break;
      }
    }
    dir = dirname(dir);
  }

  if (!version) {
    throw new Error('Could not locate the rrweb package.json to read its version');
  }

  console.info(`Found rrweb version ${version} (${packageJsonPath})`);
} catch (err) {
  console.error('Could not generate rrweb version file');
  console.error(err);
  process.exit(1);
}

writeFileSync(
  'src/rrwebVersion.ts',
  `// auto-generated by bin/genRrwebVersion.js
export const RRWEB_VERSION = '${version}';
`
);
